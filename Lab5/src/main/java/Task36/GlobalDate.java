package Task36;

public abstract class GlobalDate {
    abstract int getDay();
    abstract void setDay(int day);
    abstract int getMonth();
    abstract void setMonth(int month);
    abstract int getYear();
    abstract void setYear(int year);
    abstract void setMinutes(int value);
    abstract int getMinutes();
    abstract void setHours(int value);
    abstract int getHours();
}
